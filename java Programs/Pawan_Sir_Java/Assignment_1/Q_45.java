public class Q_45 {
    public static void main(String[] args) {
        double a = -21;
        double b = -18;
        double d = b - a;
        double n = 28;
        double s = (n * (2 * a + ((n - 1) * d))) / 2;
        System.out.println("Sum of 28 terms: " + s);
        // Output= 546
    }
}
